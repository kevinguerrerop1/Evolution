    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        clientes c = (clientes) rut.getSelectedItem();
        if (ftermino.getText().equals(c.getF_termino())) {
            JOptionPane.showMessageDialog(null, "Favor de ingresar nueva fecha");
        } else {
            crud.modificarCliente(c.getN_socio(), ftermino.getText());
            JOptionPane.showMessageDialog(null, "Membres√≠a Actualizada a: " + c.getNombre() + " " + c.getApellido());
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


