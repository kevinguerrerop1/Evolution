    private void llenardatos(String nombre) {
        //indicar que empezamos a trabajar de la primera columna
        tabla.setColumnCount(0);
        tabla.addColumn("NÂ°. Trabajador");
        tabla.addColumn("Nombre");
        tabla.addColumn("Cargo");
        tabla.addColumn("Sueldo");
        lista = datos.MostrarTra(nombre);
        tabla.setNumRows(lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getCodUsu(), i, 0);
            tabla.setValueAt(lista.get(i).getNomUsu(), i, 1);
            tabla.setValueAt(lista.get(i).getNomCar(), i, 2);
            tabla.setValueAt(lista.get(i).getSueldo(), i, 3);
        }
        //Entregar los datos a la tabla
        jTable1.setModel(tabla);
    }

